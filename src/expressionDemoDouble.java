public class expressionDemoDouble {
    public static void main(String[] args) {
        System.out.println(17.0 + 5.0); // 22.0
        System.out.println(17.0 - 5.0); // 12.0
        System.out.println(17.0 * 5.0); // 85.0
        System.out.println(17.0 / 5.0); // 3.4
        System.out.println(17.0 % 5.0); // 2.0

    }
}